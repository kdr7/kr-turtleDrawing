import turtle

my_turtle = turtle.Turtle()

#this draws the square
def square():
    my_turtle.forward(100)
    my_turtle.left(90)
    my_turtle.forward(100)
    my_turtle.left(90)
    my_turtle.forward(100)
    my_turtle.left(90)
    my_turtle.forward(100)

square()
my_turtle.forward(100)
square()

